// For each file, extract the WOCS_PAGE_TR, add missing keys, write back
files.forEach(filePath => {
  const fullPath = path.join(__dirname, filePath);
  let content = fs.readFileSync(fullPath, 'utf8');

  // Find the WOCS_PAGE_TR block - handle both formats
  // Format 1: <script>var WOCS_PAGE_TR={...};\n</script>
  // Format 2: var WOCS_PAGE_TR={...};  (inside a script block with other content)

  const trMatch = content.match(/var WOCS_PAGE_TR=(\{[\s\S]*?\});/);
  if (!trMatch) {
    console.log(`SKIP: No WOCS_PAGE_TR found in ${filePath}`);
    return;
  }

  const trString = trMatch[1];
  const trStart = trMatch.index;
  const fullMatch = trMatch[0];

  // Parse the translation object
  let tr;
  try {
    tr = eval('(' + trString + ')');
  } catch(e) {
    console.log(`ERROR parsing ${filePath}: ${e.message}`);
    return;
  }

  // Get all keys from ko (primary)
  const koKeys = tr.ko ? Object.keys(tr.ko) : [];
  const enKeys = tr.en ? Object.keys(tr.en) : [];
  const allKeys = [...new Set([...koKeys, ...enKeys])];

  const langs13 = ['ja','zh','es','fr','de','pt','it','ar','ru','tr','tw','id','th'];

  let changed = false;

  langs13.forEach(lang => {
    if (!tr[lang]) tr[lang] = {};

    allKeys.forEach(key => {
      if (tr[lang][key] !== undefined) return; // already exists

      changed = true;
      const koVal = tr.ko ? tr.ko[key] : undefined;
      const enVal = tr.en ? tr.en[key] : undefined;

      // Generate translation
      tr[lang][key] = translateKey(lang, key, koVal, enVal, tr);
    });
  });

  // Also check if en is missing keys that ko has
  if (tr.en) {
    koKeys.forEach(key => {
      if (tr.en[key] === undefined) {
        changed = true;
        tr.en[key] = tr.ko[key]; // fallback
      }
    });
  }

  if (!changed) {
    console.log(`OK: ${filePath} - no missing translations`);
    return;
  }

  // Serialize back
  const newTrString = serializeTR(tr);
  const newFullMatch = 'var WOCS_PAGE_TR=' + newTrString + ';';

  content = content.replace(fullMatch, newFullMatch);
  fs.writeFileSync(fullPath, content, 'utf8');

  // Count
  let totalAdded = 0;
  langs13.forEach(lang => {
    const origKeys = eval('(' + trString + ')')[lang] || {};
    const newKeys = tr[lang];
    totalAdded += Object.keys(newKeys).length - Object.keys(origKeys).length;
  });
  console.log(`DONE: ${filePath} - added ${totalAdded} translations`);
});

function translateKey(lang, key, koVal, enVal, tr) {
  if (!koVal && !enVal) return '';
  const ko = koVal || '';
  const en = enVal || '';

  // Use lookup tables for known translations
  const result = getTranslation(lang, ko, en, key);
  return result;
}

function getTranslation(lang, ko, en, key) {
  // Large translation mapping by language
  const translations = {
    ja: translateJa,
    zh: translateZh,
    es: translateEs,
    fr: translateFr,
    de: translateDe,
    pt: translatePt,
    it: translateIt,
    ar: translateAr,
    ru: translateRu,
    tr: translateTr,
    tw: translateTw,
    id: translateId,
    th: translateTh,
  };

  return translations[lang](ko, en, key);
}


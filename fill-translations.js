function serializeTR(tr) {
  const langs = ['ko','en','ja','zh','es','fr','de','pt','it','ar','ru','tr','tw','id','th'];
  const parts = [];
  for (const lang of langs) {
    if (!tr[lang]) continue;
    const entries = [];
    // Sort keys naturally (p1, p2, ... p10, p11, etc + special keys)
    const keys = Object.keys(tr[lang]).sort((a, b) => {
      const aNum = a.match(/^p(\d+)$/);
      const bNum = b.match(/^p(\d+)$/);
      if (aNum && bNum) return parseInt(aNum[1]) - parseInt(bNum[1]);
      if (aNum) return -1;
      if (bNum) return 1;
      return a.localeCompare(b);
    });
    for (const k of keys) {
      const v = tr[lang][k];
      const escaped = v.replace(/\\/g, '\\\\').replace(/"/g, '\\"').replace(/\n/g, '\\n');
      entries.push(`${k}:"${escaped}"`);
    }
    parts.push(`${lang}:{${entries.join(',')}}`);
  }
  return '{' + parts.join(',\n') + '\n}';
}

